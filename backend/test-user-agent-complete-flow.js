// Helper function to make API calls
async function makeChatRequest(message, step = 0, questionMode = true, currentQuestion = null, predefinedQuestions = [], isFinalQuestionFlag = false, followUpMode = true) {
  try {
    const response = await axios.post(`${BASE_URL}/api/chat`, {
      message,
      step,
      questionMode,
      currentQuestion,
      predefinedQuestions,
      isFinalQuestionFlag,
      followUpMode,
      sessionId: TEST_SESSION_ID
    });
    
    return response.data;
  } catch (error) {
    console.error('âŒ API call failed:', error.response?.data || error.message);
    throw error;
  }
}

// Helper function to wait between requests
function delay(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

